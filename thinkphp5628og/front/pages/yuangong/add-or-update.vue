					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数



			// 下拉变化
			xingbieChange(e) {
				this.xingbieIndex = e.target.value
				this.ruleForm.xingbie = this.xingbieOptions[this.xingbieIndex]
			},
            async bumenChange1(e) {
                this.bumenIndex = e.target.value
                this.ruleForm.bumen = this.bumenOptions[this.bumenIndex]
                this.ruleForm.zhiwei = '';
                this.zhiweiOptions = [];
                this.zhiweiIndex = 0;
                let res = await this.$api.option(`zhiwei`, `zhiwei`, {conditionColumn:'bumen',conditionValue:this.bumenOptions[this.bumenIndex]});
                if (res && res.code === 0) {
                      this.zhiweiOptions = res.data;
                      this.zhiweiOptions.unshift("请选择职位");
                }
            },
            // 下拉变化
            zhiweiChange(e) {
                    this.zhiweiIndex = e.target.value
                    this.ruleForm.zhiwei = this.zhiweiOptions[this.zhiweiIndex]
            },

			touxiangTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.touxiang = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

