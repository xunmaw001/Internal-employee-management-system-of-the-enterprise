	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				yuangonggonghao: '',
				mima: '',
				yuangongxingming: '',
				xingbie: '',
				touxiang: '',
				bumen: '',
				zhiwei: '',
				shouji: '',
				shenfenzheng: '',
				},
				xingbieOptions: [],
				xingbieIndex: 0,
				bumenOptions: [],
				bumenIndex: 0,
				zhiweiOptions: [],
				zhiweiIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   yuangonggonghao : false,
                                   mima : false,
                                   yuangongxingming : false,
                                   xingbie : false,
                                   touxiang : false,
                                   bumen : false,
                                   zhiwei : false,
                                   shouji : false,
                                   shenfenzheng : false,
                                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

            this.ro.bumen = true;
            this.ro.zhiwei = true;

			// 自定义下拉框值
			this.xingbieOptions = "男,女".split(',')
			// 下拉框
			res = await this.$api.option(`bumen`,`bumen`,{});
			this.bumenOptions = res.data;
            this.bumenOptions.unshift("请选择部门");

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yuangong`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yuangonggonghao'){
					this.ruleForm.yuangonggonghao = obj[o];
					this.ro.yuangonggonghao = true;
					continue;
					}
					if(o=='mima'){
					this.ruleForm.mima = obj[o];
					this.ro.mima = true;
					continue;
					}
					if(o=='yuangongxingming'){
					this.ruleForm.yuangongxingming = obj[o];
					this.ro.yuangongxingming = true;
					continue;
					}
					if(o=='xingbie'){
					this.ruleForm.xingbie = obj[o];
					this.ro.xingbie = true;
					continue;
					}
					if(o=='touxiang'){
					this.ruleForm.touxiang = obj[o];
					this.ro.touxiang = true;
					continue;
					}
					if(o=='bumen'){
					this.ruleForm.bumen = obj[o];
					this.ro.bumen = true;
					continue;
					}
					if(o=='zhiwei'){
					this.ruleForm.zhiwei = obj[o];
					this.ro.zhiwei = true;
					continue;
					}
					if(o=='shouji'){
					this.ruleForm.shouji = obj[o];
					this.ro.shouji = true;
					continue;
					}
					if(o=='shenfenzheng'){
					this.ruleForm.shenfenzheng = obj[o];
					this.ro.shenfenzheng = true;
					continue;
