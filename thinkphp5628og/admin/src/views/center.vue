        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0","display":"inline-block"}' v-if="flag=='yuangong'"  label="职位" prop="zhiwei">
          <el-select v-model="ruleForm.zhiwei" :disabled="true" placeholder="请选择职位">
            <el-option
                v-for="(item,index) in yuangongzhiweiOptions"
                v-bind:key="index"
                :label="item"
